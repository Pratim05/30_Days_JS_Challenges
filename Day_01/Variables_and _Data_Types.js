// Activity 1: Variable Declaration

var num = 59
console.log(num) // 59

let myName = 'Pratim Bera'
console.log(myName) // Pratim Bera

// Activity 2: Constant Declaration

const bool = false
console.log(bool) // false

// Activity 3: Data Types
data = {
 name:'Pratim',
 age : 10
}
array = [10,'Pratim',true]
console.log('Type of Data : ',typeof(data))     // object
console.log('Type of array : ',typeof(array))   // object
console.log('Type of num : ',typeof(num))       // number
console.log('Type of myName : ',typeof(myName)) // string
console.log('Type of bool : ',typeof(bool))     // boolean


// Activity 4: Reassigning Variables

let value = 10
console.log(value)  // 10
value = 100
console.log(value)  // 100

// Activity 5: Understanding const

const age = 20
age = 21  // TypeError: Assignment to constant variable.
